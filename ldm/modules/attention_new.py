class MemoryEfficientRestormerAttention(nn.Module):
    # https://github.com/MatthieuTPHR/diffusers/blob/d80b531ff8060ec1ea982b65a1b8df70f73aa67c/src/diffusers/models/attention.py#L223
    def __init__(self, query_dim, context_dim=None, heads=8, dim_head=64, dropout=0.0):
        super().__init__()
        print(f"Setting up {self.__class__.__name__}. Query dim is {query_dim}, context_dim is {context_dim} and using "
              f"{heads} heads.")
        inner_dim = dim_head * heads
        context_dim = default(context_dim, query_dim)

        self.heads = heads
        self.dim_head = dim_head

        self.to_qkv = nn.Conv2d(query_dim, inner_dim*3, kernel_size=1, bias=False)
        self.qkv_dwconv = nn.Conv2d(inner_dim*3, inner_dim*3, kernel_size=3, stride=1, padding=1, groups=inner_dim*3, bias=False)

        self.to_out = nn.Sequential(zero_module(nn.Linear(inner_dim, query_dim)), nn.Dropout(dropout))
        self.attention_op: Optional[Any] = None

    def forward(self, x, context=None, mask=None):
        assert context == None
        b, hw, d = x.shape
        hw_ = int(hw**0.5)
        x = x.view(b, hw_, hw_, d)
        x = rearrange(x, 'b h w c -> b c h w').contiguous()
        qkv = self.qkv_dwconv(self.to_qkv(x))
        q,k,v = qkv.chunk(3, dim=1)
        q = q.flatten(2).transpose(1, 2)
        k = k.flatten(2).transpose(1, 2)
        v = v.flatten(2).transpose(1, 2)

        b, _, _ = q.shape
        q, k, v = map(
            lambda t: t.unsqueeze(3)
            .reshape(b, t.shape[1], self.heads, self.dim_head)
            .permute(0, 2, 3, 1)
            .reshape(b * self.heads, self.dim_head, t.shape[1])
            .contiguous(),
            (q, k, v),
        )

        # actually compute the attention, what we cannot get enough of
        out = xformers_state.xformers.ops.memory_efficient_attention(q, k, v, attn_bias=None, op=self.attention_op)

        if exists(mask):
            raise NotImplementedError
        out = (
            out.unsqueeze(0)
            .reshape(b, self.heads, self.dim_head, out.shape[2])
            .permute(0, 3, 1, 2)
            .reshape(b, out.shape[2], self.heads * self.dim_head)
        )
        return self.to_out(out)


